package com.rizkiashari.restoapp.model;

public class MenuModel {
    private int id;
    private String nameMenu, pictureMenu, priceMenu;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNameMenu() {
        return nameMenu;
    }

    public void setNameMenu(String nameMenu) {
        this.nameMenu = nameMenu;
    }

    public String getPictureMenu() {
        return pictureMenu;
    }

    public void setPictureMenu(String pictureMenu) {
        this.pictureMenu = pictureMenu;
    }

    public String getPriceMenu() {
        return priceMenu;
    }

    public void setPriceMenu(String priceMenu) {
        this.priceMenu = priceMenu;
    }


}
